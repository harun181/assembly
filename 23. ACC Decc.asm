INCLUDE 'EMU8086.INC'
.MODEL SMALL
.STACK 100H

.DATA
ARRAY DB ? 
N DB ?

.CODE
MAIN PROC
 MOV AX,@DATA
 MOV DS,AX 
 XOR CX,CX 
 XOR AX,AX
 
 PRINT 'ENTER ARRAY SIZE : '
 MOV AH,1
 INT 21H
 SUB AL,48
 MOV BL,AL 
 
 PRINTN
 PRINTN
 PRINT 'ENTER ARRAY ELEMENTS : '
 MOV CL,AL 
 MOV SI,0
 
 INPUT:
 MOV AH,1
 INT 21H
 MOV ARRAY[SI],AL
 INC SI
 LOOP INPUT
 
 
 CALL ACCENDING 
 
 CALL DECENDING
 
 MAIN ENDP 
ACCENDING PROC
 
 MOV CL,BL
 DEC CL
 OUTER_LOOP: 
 
 MOV BH,CL
 MOV SI,0
 
 INNER_LOOP: 
 
 MOV AL,ARRAY[SI]
 MOV DL,ARRAY[SI+1]
 
 CMP AL,DL
 JC NO_SWAP
 MOV ARRAY[SI],DL
 MOV ARRAY[SI+1],AL
 
 NO_SWAP:
 INC SI
 DEC BH
 JNZ INNER_LOOP 
 
 LOOP OUTER_LOOP 
 
 PRINTN
 PRINTN
 PRINT 'ACCENDING ORDER : '
 MOV CL,BL
 MOV SI,0 
 OUTPUT1:
 MOV DL,ARRAY[SI] 
 MOV AH,2
 INT 21H 
 MOV DL,' '
 MOV AH,2
 INT 21H
 INC SI
 LOOP OUTPUT1
 
 RET 
 
 ACCENDING ENDP
 
 
DECENDING PROC
 
 MOV CL,BL
 DEC CL
 OUTER_LOOP1: 
 
 MOV BH,CL
 MOV SI,0
 
 INNER_LOOP1: 
 
 MOV AL,ARRAY[SI]
 MOV DL,ARRAY[SI+1]
 
 CMP AL,DL
 JNC NO_SWAP1
 MOV ARRAY[SI],DL
 MOV ARRAY[SI+1],AL
 
 NO_SWAP1:
 INC SI
 DEC BH
 JNZ INNER_LOOP1 
 
 LOOP OUTER_LOOP1 
 
 PRINTN
 PRINTN
 PRINT 'DECENDING ORDER : '
 MOV CL,BL
 MOV SI,0 
 OUTPUT2:
 MOV DL,ARRAY[SI] 
 MOV AH,2
 INT 21H 
 MOV DL,' '
 MOV AH,2
 INT 21H
 INC SI
 LOOP OUTPUT2 
 
 MOV AH,4CH
 
END MAIN