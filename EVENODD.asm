org 100h

.DATA

FA DB 'ENTER A NUMBER: $'
OP DB 'RESULT IS: $'
EVEN DB 'EVEN $'
ODD DB 'ODD $'
N DB ?

NEXT DB 0DH, 0AH, '$'

.CODE

MOV AX, @DATA
MOV DS, AX

; FIRST INPUT

MOV AH, 9
LEA DX, NEXT
INT 21H

LEA DX, FA
INT 21H

MOV AH, 1
INT 21H

MOV N, AL

MOV BL, 2
DIV BL
MOV N, AH

; RESULT

MOV AH, 9
LEA DX, NEXT
INT 21H

LEA DX, OP
INT 21H

; STATEMENT

MOV AH, 9

CMP N, 0
JE RESULT

LEA DX, ODD
INT 21H
JMP END

RESULT:
LEA DX, EVEN
INT 21H
JMP END

END:
MOV AH, 4CH
INT 21H

ret
